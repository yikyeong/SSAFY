bit = "0000000111100000011000000111100110000110000111100111100111111001100111"
# 출력결과 : "0 120 12 7 76 24 60 121 124 103"

# 이진수를 7칸씩 쪼개서 십진수로 만들기
N = len(bit)

for i in range(0,N,7):
    dec = 0 # 십진수로 바꾼 결과
    for j in range(6, -1, -1):
        dec += (2 ** (6 - j)) * int(bit[i + j])

    print(dec, end=" ")